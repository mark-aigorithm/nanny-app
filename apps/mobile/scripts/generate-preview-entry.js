#!/usr/bin/env node
/**
 * Generates src/preview-entry-generated.tsx from the COMPONENT env var.
 *
 * Usage (called automatically by the preview:web script):
 *   COMPONENT=src/components/MyScreen.tsx node scripts/generate-preview-entry.js
 */
const fs = require('fs');
const path = require('path');

const componentPath = process.env.COMPONENT;
if (!componentPath) {
  console.error('Error: COMPONENT env var is required.');
  console.error('  Example: COMPONENT=src/components/Foo.tsx npm run preview:web');
  process.exit(1);
}

// Resolve relative to src/preview-entry-generated.tsx → src/
// so the import path should be relative to src/
const absoluteComponent = path.resolve(process.cwd(), componentPath);
const generatedFile = path.resolve(process.cwd(), 'src/preview-entry-generated.tsx');
const generatedDir = path.dirname(generatedFile);
let importPath = path.relative(generatedDir, absoluteComponent);
// Normalise to forward slashes and strip .tsx extension for the import
importPath = importPath.replace(/\\/g, '/').replace(/\.tsx?$/, '');
// Ensure it starts with ./ or ../
if (!importPath.startsWith('.')) importPath = './' + importPath;

const content = `// AUTO-GENERATED by scripts/generate-preview-entry.js — do not edit manually.
// Regenerated each time \`npm run preview:web\` runs.
import React from 'react';
import { View, StyleSheet } from 'react-native';
import Component from '${importPath}';

export default function PreviewWrapper() {
  return (
    <View style={styles.root}>
      <Component />
    </View>
  );
}

const styles = StyleSheet.create({
  root: {
    flex: 1,
    alignItems: 'center',
    justifyContent: 'center',
    backgroundColor: '#ffffff',
    width: 390,
    height: 844,
  },
});
`;

fs.writeFileSync(generatedFile, content, 'utf8');
console.log(`Generated preview entry for: ${componentPath}`);
